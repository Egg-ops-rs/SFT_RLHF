#!/bin/bash

# ================================================================================
#  文件作用说明：
# 这是单机多卡训练的启动脚本（使用torchrun方式）
# 
# 原因：项目使用的是DeepSpeed分布式训练，而不是torchrun方式
# 实际使用的是：deepspeed_cluster_launcher.sh
# 
#  原本的设计作用：
# 1. 在单台服务器上使用4张GPU进行训练
# 2. 使用PyTorch原生的torchrun进行分布式训练
# 3. 提供后台训练和日志管理功能
# 
#  保留原因：作为备用方案，如果需要单机训练时可以参考
# ================================================================================

# 4张GPU训练脚本（nohup模式）
echo " 开始后台训练金融奖励模型（4张4090 + DeepSpeed Stage 2）"

# ============================================================================
#  环境变量设置：配置CUDA和训练相关的环境参数
# 
#  这些变量的作用：
# 1. CUDA_VISIBLE_DEVICES：告诉程序使用哪些GPU（0,1,2,3表示使用前4张卡）
# 2. NCCL_DEBUG：启用NCCL通信库的调试信息，帮助排查分布式训练问题
# 3. OMP_NUM_THREADS：限制OpenMP线程数，避免CPU资源竞争
# ============================================================================
export CUDA_VISIBLE_DEVICES=0,1,2,3  # 指定使用GPU 0,1,2,3
export NCCL_DEBUG=INFO                # 启用NCCL调试信息
export OMP_NUM_THREADS=1              # 设置OpenMP线程数为1

# ============================================================================
#  多机多卡配置：定义分布式训练的网络参数
# 
#  这些参数的作用：
# 1. MASTER_ADDR：主节点的IP地址，所有节点都要连接到这个地址
# 2. MASTER_PORT：主节点的端口号，用于节点间通信
# 3. NNODES：总节点数（这里设为1，表示单机训练）
# 4. NODE_RANK：当前节点的编号（从0开始）
# 5. NPROC_PER_NODE：每个节点的进程数（等于GPU数量）
# ============================================================================
MASTER_ADDR=${MASTER_ADDR:-"localhost"}  # 默认主节点地址为localhost
MASTER_PORT=${MASTER_PORT:-29500}        # 默认端口号为29500
NNODES=${NNODES:-1}                      # 默认节点数为1（单机）
NODE_RANK=${NODE_RANK:-0}                # 默认节点编号为0（主节点）
NPROC_PER_NODE=${NPROC_PER_NODE:-4}      # 默认每节点4个进程（4张GPU）

# ============================================================================
#  项目路径设置：确定项目根目录并切换到该目录
# 
#  这段代码的作用：
# 1. 自动找到项目的根目录（向上查找两级目录）
# 2. 切换到项目根目录，确保所有相对路径都是正确的
# 3. 打印项目信息，便于确认配置
# ============================================================================
PROJECT_ROOT=$(cd "$(dirname "$0")/../.." && pwd)  # 获取项目根目录的绝对路径
cd $PROJECT_ROOT  # 切换到项目根目录

echo " 项目根目录: $PROJECT_ROOT"
echo " GPU配置: 4张RTX 4090"
echo " 批次配置: 总批次大小 = 4 GPU × 2 batch × 4 accumulation = 32"

# ============================================================================
#  预检查：训练开始前的环境和数据检查
# 
#  这个检查的作用：
# 1. 验证训练数据是否存在且格式正确
# 2. 验证模型文件是否存在且完整
# 3. 如果检查失败，提前终止，避免浪费计算资源
# 
# ============================================================================
echo " 检查数据和模型..."
python scripts/setup/check_data.py  # 执行数据和模型检查脚本
if [ $? -ne 0 ]; then              # 如果检查脚本返回非0值（表示失败）
    echo "❌ 数据或模型检查失败，退出训练"
    exit 1                          # 退出脚本
fi

echo "✅ 所有检查通过"

# ============================================================================
#  日志管理：创建日志目录和文件名
# 
#  这个设置的作用：
# 1. 为每次训练创建独特的日志文件（使用时间戳）
# 2. 保存进程PID，便于后续管理和停止训练
# 3. 将所有输出重定向到日志文件，便于后续分析
# ============================================================================
mkdir -p logs  # 创建logs目录（如果不存在）

# 生成时间戳，格式：年月日_时分秒
TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
LOG_FILE="logs/training_${TIMESTAMP}.log"      # 日志文件路径
PID_FILE="logs/training_${TIMESTAMP}.pid"      # PID文件路径

echo " 日志文件: $LOG_FILE"
echo " PID文件: $PID_FILE"

# ============================================================================
#  启动训练：使用nohup和torchrun启动后台训练
# 
#  这个命令的作用：
# 1. nohup：让训练在后台运行，即使终端关闭也不会停止
# 2. torchrun：PyTorch的分布式训练启动器
# 3. 各种参数告诉torchrun如何分配GPU和管理进程
# 
# ⚠️ 重要：这个方式在当前项目中未被使用，实际使用的是DeepSpeed
# ============================================================================
echo " 启动后台训练..."
nohup torchrun \
    --nnodes=$NNODES \                    # 节点数量
    --nproc_per_node=$NPROC_PER_NODE \    # 每节点进程数
    --node_rank=$NODE_RANK \              # 节点编号
    --master_addr=$MASTER_ADDR \          # 主节点地址
    --master_port=$MASTER_PORT \          # 主节点端口
    src/train_reward_model.py \           # 训练脚本路径
    configs/training/config.json \       # 配置文件路径（注意：这个配置文件不存在）
    > $LOG_FILE 2>&1 &                   # 重定向输出到日志文件并后台运行

# ============================================================================
#  保存PID和提供监控信息
# 
#  这部分的作用：
# 1. 保存训练进程的PID，便于后续停止训练
# 2. 向用户提供各种监控和管理命令
# 3. 让用户知道如何查看训练状态和停止训练
# ============================================================================
TRAIN_PID=$!                    # 获取后台进程的PID
echo $TRAIN_PID > $PID_FILE     # 将PID保存到文件

echo " 训练已在后台启动！"
echo " 进程ID: $TRAIN_PID"
echo " 日志文件: $LOG_FILE"
echo " PID文件: $PID_FILE"
echo ""
echo " 监控命令:"
echo "  查看实时日志: tail -f -n 300 $LOG_FILE"
echo "  查看GPU使用: watch -n 1 nvidia-smi"
echo "  查看进程状态: ps aux | grep $TRAIN_PID"
echo "  停止训练: kill $TRAIN_PID"
echo "  启动tensorboard: tensorboard --logdir=./logs --port=6006"
echo ""
echo " 训练状态检查:"
echo "  ps aux | grep torchrun"
echo "  tail -20 $LOG_FILE" 
